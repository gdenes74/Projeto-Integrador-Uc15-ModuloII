    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Clientes novoCliente = new Clientes();

        try {
            //para a descrição capturamos direto o valor do campo de texto
            novoCliente.setNome_cliente(txtCliente.getText());
            novoCliente.setCpf(txtCpf.getText());
            novoCliente.setCnpj(txtCnpj.getText());
            novoCliente.setEndereço(txtEndereco.getText());
            String dataNascimentoTexto = txtDataNascimento.getText();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/y");
            LocalDate dataNascimento = LocalDate.parse(dataNascimentoTexto, formatter);
            novoCliente.setData_nascimento(dataNascimento);
            novoCliente.setE_mail(txtEmail.getText());
            novoCliente.setFone(txtFone.getText());

            ClientesDAO clientesDAO = new ClientesDAO();

            clientesDAO.cadastrar(novoCliente);

            //fechando a tela após o cadastros
            LimparCampos();
            this.dispose();

            ListagemClientes listagem = new ListagemClientes();
            listagem.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Ocorreu uma falha :\n" + e.getMessage());
        }

    }//GEN-LAST:event_btnCadastrarActionPerformed

