    public void preencherFormulario(Clientes clientes) {
        txtCliente.setText(clientes.getNome_cliente());
        txtCpf.setText(clientes.getCpf());
        txtCnpj.setText(clientes.getCnpj());
        txtEndereco.setText(clientes.getEndereco());
        txtDataNascimento.setText(clientes.getData_nascimento().format(DateTimeFormatter.ofPattern("dd/MM/y")));
        txtEmail.setText(clientes.getE_mail());
        txtFone.setText(clientes.getFone());
    }

    public void validaPermissao(int id) {
        this.id_permissao = id;
        if (id_permissao == 3) {
            btnCadastrar.setVisible(false);
            btnEditar.setVisible(false);
            btnRemover.setVisible(false);
        } else if (id_permissao == 2) {
            btnEditar.setVisible(false);
            btnRemover.setVisible(false);
        }
    }

