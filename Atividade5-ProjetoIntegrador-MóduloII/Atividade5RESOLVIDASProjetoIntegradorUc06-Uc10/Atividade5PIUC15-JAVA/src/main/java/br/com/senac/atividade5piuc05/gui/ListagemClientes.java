    public void preencheTabela(List<Clientes> clientes) {
        String columns[] = {"Id_Clientes", "Nome_cliente", "Cpf", "Cnpj", "Endereço", "Data_nascimento", "e_mail", "Fone"};
        String dados[][] = new String[clientes.size()][columns.length];

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/y");
        int i = 0;
        for (Clientes c : clientes) {
            String dataNascimentoTexto;
            if (c.getData_nascimento() != null) {
                dataNascimentoTexto = c.getData_nascimento().format(formatter);
            } else {
                dataNascimentoTexto = "";
            }
            dados[i] = new String[]{
                String.valueOf(c.getId_Clientes()),
                c.getNome_cliente(),
                c.getCpf(),
                c.getCnpj(),
                c.getEndereco(),
                dataNascimentoTexto,
                c.getE_mail(),
                c.getFone()
            };

            i++;
        }

        DefaultTableModel model = new DefaultTableModel(dados, columns);
        tblClientes.setModel(model);
    }

